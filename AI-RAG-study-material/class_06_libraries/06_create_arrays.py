import numpy as np

# Create an array of three zeros.
print("Zeros:", np.zeros(3))

# Create a 2x2 array of ones.
print("Ones:", np.ones((2, 2)))

# Create an array from 1 to 9 in steps of 2.
print("Arange:", np.arange(1, 10, 2))

# Create 5 evenly spaced numbers between 0 and 1.
print("Linspace:", np.linspace(0, 1, 5))
